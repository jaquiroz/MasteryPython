quote = 'to be or not to be'

# .upper convierte a todo el string en mayusculas
print(quote.upper())

# .capitalize convierte la primera letra de lstring en mayuscula
print(quote.capitalize())

# .find encuentra la ubucacion de un tipo de string presente en la variable
print(quote.find('be'))

# .count cuenta las veces que se repite el parametro indicado
print(quote.count('be'))

# .replace intercambia una seccion del strings por otra
print(quote.replace('be', 'me'))

#Todos los metodos crean un nuevo string no modifican el original debido al concepto
#de inmutabilidad.
