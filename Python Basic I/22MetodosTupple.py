#Tupple
my_tupple = (1,2,3,4,5,5)

print(my_tupple.count(5)) #Devuelve la cantidad de veces que se repite el parametro

print(my_tupple.index(5)) #Devuelve el indice del paramatro dentro de tupple

print(len(my_tupple)) #Devuelve la longitud de tupple

